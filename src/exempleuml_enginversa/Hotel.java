package exempleuml_enginversa;

/**
 *
 * @author armand
 */
public class Hotel {
    private String nom;
    private String categoria;
    private String telefon;
    private String ciutat;
    private int quantPlaces;

    /**
     * @return the nom
     */
    public String getNom() {
        return nom;
    }

    /**
     * @param nom the nom to set
     */
    public void setNom(String nom) {
        this.nom = nom;
    }

    /**
     * @return the categoria
     */
    public String getCategoria() {
        return categoria;
    }

    /**
     * @param categoria the categoria to set
     */
    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    /**
     * @return the telefon
     */
    public String getTelefon() {
        return telefon;
    }

    /**
     * @param telefon the telefon to set
     */
    public void setTelefon(String telefon) {
        this.telefon = telefon;
    }

    /**
     * @return the ciutat
     */
    public String getCiutat() {
        return ciutat;
    }

    /**
     * @param ciutat the ciutat to set
     */
    public void setCiutat(String ciutat) {
        this.ciutat = ciutat;
    }

    /**
     * @return the quantPlaces
     */
    public int getQuantPlaces() {
        return quantPlaces;
    }

    /**
     * @param quantPlaces the quantPlaces to set
     */
    public void setQuantPlaces(int quantPlaces) {
        this.quantPlaces = quantPlaces;
    }

    



}
